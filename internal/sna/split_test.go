package sna

import "testing"

func TestSplitPIUs(t *testing.T) {
	// The real 45-byte coalesced read from SNA Server: ACTLU +RSP (25 bytes)
	// followed by a NOTIFY (20 bytes).
	actlu := []byte{0x2D, 0x00, 0x00, 0x02, 0x00, 0x02, 0xEB, 0x80, 0x00, 0x0D, 0x01, 0x01, 0x00, 0x85, 0x80, 0x00, 0x00, 0x0C, 0x06, 0x01, 0x00, 0x01, 0x00, 0x00, 0x00}
	notify := []byte{0x2C, 0x00, 0x00, 0x02, 0x00, 0x00, 0x0B, 0x80, 0x00, 0x81, 0x06, 0x20, 0x0C, 0x06, 0x01, 0x00, 0x01, 0x00, 0x00, 0x00}
	buf := append(append([]byte{}, actlu...), notify...)

	got := SplitPIUs(buf)
	if len(got) != 2 {
		t.Fatalf("SplitPIUs returned %d PIUs, want 2", len(got))
	}
	if string(got[0]) != string(actlu) {
		t.Errorf("PIU[0] = % X, want % X", got[0], actlu)
	}
	if string(got[1]) != string(notify) {
		t.Errorf("PIU[1] = % X, want % X", got[1], notify)
	}
}

func TestSplitPIUsSingle(t *testing.T) {
	// A lone PIU whose RU contains a 0x2x byte (the NOTIFY's 0x20) must not split.
	notify := []byte{0x2C, 0x00, 0x00, 0x02, 0x00, 0x00, 0x0B, 0x80, 0x00, 0x81, 0x06, 0x20, 0x0C, 0x06, 0x03, 0x00, 0x01, 0x00, 0x00, 0x00}
	got := SplitPIUs(notify)
	if len(got) != 1 {
		t.Fatalf("SplitPIUs split a single PIU into %d", len(got))
	}
}
